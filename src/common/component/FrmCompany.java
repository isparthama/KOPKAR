    public void setData() throws CodeException {
        
          if (istatus==1){
              obj.insert();
          }
          obj.setCompany(jtxtCompany.getText()) ;
          obj.setDescription(jtxtDescription.getText()) ;
          obj.setDatabase(jtxtDatabase.getText()) ;
          

    }

    public void undo() throws CodeException {
        istatus = 0;
        obj=objtmp;
        moveFirst();
        showData();
    }

