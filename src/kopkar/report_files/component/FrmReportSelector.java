    private void showreports() {
        try {
            orpt.initdata(this.report_category_id);
            jComboBox1.removeAllItems();
            for (Object o:orpt){
                report_filesext rpt=(report_filesext) o;
                jComboBox1.addItem(rpt.getreport_desc());
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrmReportSelector.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

