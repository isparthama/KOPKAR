    private void cekdetail() {
        int row = jtbldetail.getSelectedRow();
        int col = jtbldetail.getSelectedColumn();


        ko_transaksi_bayar_toko_detail1ext appod=new ko_transaksi_bayar_toko_detail1ext(0);
        if (row > appods.size() - 1) {
            appod = new ko_transaksi_bayar_toko_detail1ext(0);
            appod.setstatus(ko_transaksi_bayar_toko_detail1ext.Status_Insert);
            appods.add(appod);
        } else {
            appod = (ko_transaksi_bayar_toko_detail1ext) appods.get(row);
            if (!appod.getStatus().equals(ko_transaksi_bayar_toko_detail1ext.Status_Insert)&&!appod.getStatus().equals(ko_transaksi_bayar_toko_detail1ext.Status_delete)){
                appod.setstatus(ko_transaksi_bayar_toko_detail1ext.Status_update);
            }
        }
        switch (appod.getline_number()){
            case 1:
                appod.setstatus(ko_transaksi_bayar_toko_detail1ext.Status_Insert);
                break;
            case 2:
                appod.setstatus(ko_transaksi_bayar_toko_detail1ext.Status_update);
                break;
            case 3:
                break;
        }
        appod.setSelected((Boolean)model.getValueAt(row, 0));
        appod.setmember_number(GlobalUtils.churuf(model.getValueAt(row, 1)));
        
        appod.setpayment_amount(GlobalUtils.toDbl(GlobalUtils.churuf(model.getValueAt(row, 8))));

        appods.set(row, appod);

    }


