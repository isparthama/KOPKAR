private void jtbldetailKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtbldetailKeyPressed
    // TODO add your handling code here:
    int col=jtbldetail.getSelectedColumn();
    int row=jtbldetail.getSelectedRow();
    switch (evt.getKeyCode()){
        case KeyEvent.VK_F5:
            switch (jtbldetail.getSelectedColumn()){
                case 1:
                    obrowse.showbrowser(obrowse.ko_anggota, "", jtbldetail);
                    showanggota((Integer)jtbldetail.getSelectedRow());
                    jtbldetailPropertyChange(null);
                    GlobalUtils.selectCellTable(jtbldetail, row, 3);
                    break;
            }
            break;
        case KeyEvent.VK_INSERT:
            evt.consume();
            insertnewlastrow();
            break;
        case KeyEvent.VK_DELETE:
            evt.consume();
            if (jtbldetail.getSelectedRow()<appods.size()){
            ko_transaksi_bayar_toko_detail1ext o = appods.get(jtbldetail.getSelectedRow());
                o.setstatus(ko_transaksi_bayar_toko_detail1ext.Status_delete);
            }
            jtbldetailPropertyChange(null);
            break;
        case KeyEvent.VK_ENTER:
            evt.consume();
            break;
    }
}//GEN-LAST:event_jtbldetailKeyPressed

private void jtbldetailPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_jtbldetailPropertyChange
    // TODO add your handling code here:
    int irow=jtbldetail.getSelectedRow();
    int icol=jtbldetail.getSelectedColumn();

    if (irow>-1){
        switch (icol){
            case 0:
                set_pilih(irow);
                break;
            case 1:
                break;
            case 7:
                set_bayar(irow);
                break;
        }
        if (appods.size()>0){
        cekdetail();
        }
        tampil_total();
    }
}//GEN-LAST:event_jtbldetailPropertyChange
private void set_pilih(int irow){
    ko_transaksi_bayar_toko_detail1ext o = obj.getDetail1().get(irow);
    if (Boolean.parseBoolean(GlobalUtils.churuf(jtbldetail.getValueAt(irow, 0)))){
        if (o.getinvoice_balance()>obj.getSisa_belum_dibayar()){
            o.setpayment_amount(obj.getSisa_belum_dibayar());
        } else {
            o.setpayment_amount(o.getinvoice_balance());
        }
    } else {
        o.setpayment_amount(new Double(0));
    }
    if (o.getpayment_amount()<=0){
        jtbldetail.setValueAt(false,irow, 0);
    }
    jtbldetail.setValueAt(o.getpayment_amount(), irow, 8);
    jtbldetail.setValueAt(o.getsisa_bayar(), irow, 9);
}
private void set_bayar(int irow){
    ko_transaksi_bayar_toko_detail1ext o = obj.getDetail1().get(irow);
    o.setpayment_amount(GlobalUtils.toDbl(GlobalUtils.churuf(model.getValueAt(irow, 7))));
    if (o.getpayment_amount()>obj.getSisa_belum_dibayar()){
        o.setpayment_amount(obj.getSisa_belum_dibayar());
    }
    if (o.getpayment_amount()<=0){
        jtbldetail.setValueAt(false,irow, 0);
    } else {
        jtbldetail.setValueAt(true,irow, 0);
    }
    jtbldetail.setValueAt(o.getpayment_amount(), irow, 7);
    jtbldetail.setValueAt(o.getinvoice_balance(), irow, 8);
    
}
