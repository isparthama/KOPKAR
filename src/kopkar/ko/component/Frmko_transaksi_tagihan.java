    private void pilihsemua(boolean b) {
        Double jml_terima=GlobalUtils.toDbl(jtxtterima_bayar.getText());
        Double jml_dibayar=new Double(0);
        Double jml_sisa=jml_terima;
        Double jml_bayar=new Double(0);
        for (int i=0;i<obj.getDetail1().size()&&jml_sisa>0;i++){
            model.setValueAt(b, i, 0);
            ko_transaksi_tagihan_detail1ext o = obj.getDetail1().get(i);
            if (b){
                if (jml_sisa<GlobalUtils.toDbl(GlobalUtils.churuf(model.getValueAt(i, 6)))){
                    jml_bayar=jml_sisa;
                    model.setValueAt(jml_bayar, i, 7);
                    set_detail(i,o);
                    jtbldetail.setValueAt(o.getsisa_tagihan(), i, 8);
                    break;
                } else {
                    jml_bayar=GlobalUtils.toDbl(GlobalUtils.churuf(model.getValueAt(i, 6)));
                }
                jml_dibayar=jml_dibayar+jml_bayar;
                model.setValueAt(jml_bayar, i, 7);
                jml_sisa=jml_sisa-jml_bayar;
            }
            set_detail(i,o);
            jtbldetail.setValueAt(o.getsisa_tagihan(), i, 8);
        }
        tampil_total();
    }

    private void set_detail(int i,ko_transaksi_tagihan_detail1ext o){
        switch (o.getlinenum()){
            case 1:
                o.setstatus(ko_transaksi_tagihan_detail1ext.Status_Insert);
                break;
            case 2:
                o.setstatus(ko_transaksi_tagihan_detail1ext.Status_update);
                break;
            case 3:
                break;
        }
        o.setselected((Boolean)model.getValueAt(i, 0));
        o.setmember_number(GlobalUtils.churuf(model.getValueAt(i, 1)));

        o.settotal_tagihan(GlobalUtils.toDbl(GlobalUtils.churuf(model.getValueAt(i, 6))));
        o.settotal_bayar(GlobalUtils.toDbl(GlobalUtils.churuf(model.getValueAt(i, 7))));
    }
