    private int findintable(int searchby,String text) {
        int i=0;
        int start=0;
        if (jtbldetail.getSelectedRow()>-1){
            start=jtbldetail.getSelectedRow();
        }
        if (start==jtbldetail.getRowCount()-1){
            start=0;
        }
        for (i=start;i<obj.getDetail1().size();i++){
            ko_transaksi_tagihan_detail1ext o = obj.getDetail1().get(i);
            if (searchby==1){
                if (o.getMember_name().toLowerCase().contains(text.toLowerCase())){
                    return i;
                }
            } else {
                if (o.getmember_number().toLowerCase().contains(text.toLowerCase())){
                    return i;
                }
            }
        }
        return -1;
    }

