private void set_pilih(int irow){
    ko_transaksi_tagihan_detail1ext o = obj.getDetail1().get(irow);
    if (Boolean.parseBoolean(GlobalUtils.churuf(jtbldetail.getValueAt(irow, 0)))){
        if (o.gettotal_tagihan()>obj.getSisa_belum_dibayar()){
            o.settotal_bayar(obj.getSisa_belum_dibayar());
        } else {
            o.settotal_bayar(o.gettotal_tagihan());
        }
    } else {
        o.settotal_bayar(new Double(0));
    }
    if (o.gettotal_bayar()<=0){
        jtbldetail.setValueAt(false,irow, 0);
    }
    jtbldetail.setValueAt(o.gettotal_bayar(), irow, 7);
    jtbldetail.setValueAt(o.getsisa_tagihan(), irow, 8);
}
private void set_bayar(int irow){
    ko_transaksi_tagihan_detail1ext o = obj.getDetail1().get(irow);
    o.settotal_bayar(GlobalUtils.toDbl(GlobalUtils.churuf(model.getValueAt(irow, 7))));
    if (o.gettotal_bayar()>obj.getSisa_belum_dibayar()){
        o.settotal_bayar(obj.getSisa_belum_dibayar());
    }
    if (o.gettotal_bayar()<=0){
        jtbldetail.setValueAt(false,irow, 0);
    } else {
        jtbldetail.setValueAt(true,irow, 0);
    }
    jtbldetail.setValueAt(o.gettotal_bayar(), irow, 7);
    jtbldetail.setValueAt(o.getsisa_tagihan(), irow, 8);
    
}
