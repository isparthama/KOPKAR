    private void showbarcode() {
        GlobalModel.clearRow(model_product_barcode);
        for (int i=0;i<product_barcode.size();i++){
            in_product_barcodeext o = product_barcode.get(i);
            model_product_barcode.addRow(new Object[model_product_barcode.getColumnCount()]);
            model_product_barcode.setValueAt(o.getuom_desc(), i, 0);
            model_product_barcode.setValueAt(o.getbarcode(), i, 1);
        }
    }

    private void showinventory() {
        GlobalModel.clearRow(model_product_inventory);
        for (int i=0;i<product_inventory.size();i++){
            in_product_inventoryext o = product_inventory.get(i);
            model_product_inventory.addRow(new Object[model_product_inventory.getColumnCount()]);
            model_product_inventory.setValueAt(o.getwarehouse_code(), i, 0);
            model_product_inventory.setValueAt(o.getwarehouse_name(), i, 1);
            model_product_inventory.setValueAt(o.getonhand(), i, 2);
            model_product_inventory.setValueAt(o.getcomitted(), i, 3);
            model_product_inventory.setValueAt(o.getconsign(), i, 4);
            model_product_inventory.setValueAt(o.getisorder(), i, 5);
        }
    }

    private void cekdetail_barcode() {
        int row = jtblproduct_barcode.getSelectedRow();
        int col = jtblproduct_barcode.getSelectedColumn();

        in_product_barcodeext o = new in_product_barcodeext();
        if (row > product_barcode.size() - 1) {
            o = new in_product_barcodeext();
            o.setstatus(in_product_barcodeext.Status_Insert);
            product_barcode.add(o);
        } else {
            o = (in_product_barcodeext) product_barcode.get(row);
            if (!o.getstatus().equals(in_product_barcodeext.Status_Insert)&&!o.getstatus().equals(in_product_barcodeext.Status_delete)){
                o.setstatus(in_product_barcodeext.Status_update);
            }
        }


        product_barcode.set(row, o);

    }

    private void cekdetail() {
        int row = jtblproduct_barcode.getSelectedRow();
        int col = jtblproduct_barcode.getSelectedColumn();


        in_product_barcodeext appod=new in_product_barcodeext();
        if (row > product_barcode.size() - 1) {
            appod = new in_product_barcodeext();
            appod.setstatus(in_product_barcodeext.Status_Insert);
            product_barcode.add(appod);
        } else {
            appod = (in_product_barcodeext) product_barcode.get(row);
            if (!appod.getstatus().equals(in_product_barcodeext.Status_Insert)&&!appod.getstatus().equals(in_product_barcodeext.Status_delete)){
                appod.setstatus(in_product_barcodeext.Status_update);
            }
        }
        appod.setbarcode(GlobalUtils.churuf(model_product_barcode.getValueAt(row, 1)));
        product_barcode.set(row, appod);
    }

